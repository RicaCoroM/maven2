    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        // TODO add your handling code here:
        try{
           // Connection con= Conexion.conectarse("root",null);
           //Generamos un objeto de tipo tablita
           Tablita t=new Tablita(textoNombre.getText(),Float.parseFloat(textoSueldo.getText()));
           DAOTablita.guardar(t);
            JOptionPane.showConfirmDialog(this,"objetoguardado");
            
            textoNombre.setText(null);
            textoSueldo.setText(null);
           
        }
        catch (NumberFormatException e){
            JOptionPane.showConfirmDialog(this,"Noi metiste un numero en el sueldo");
            
        } catch(Exception e){
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_botonGuardarActionPerformed

